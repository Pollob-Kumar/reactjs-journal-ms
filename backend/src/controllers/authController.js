const User = require('../models/User');
const crypto = require('crypto');
const { sendEmail } = require('../services/emailService');

// @desc    Register user
// @route   POST /api/auth/register
// @access  Public
exports.register = async (req, res, next) => {
  try {
    const { firstName, lastName, email, password, affiliation, roles, expertise, orcid, bio } = req.body;

    // Check if user already exists
    const existingUser = await User.findOne({ email: email.toLowerCase() });
    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: 'User with this email already exists'
      });
    }

    // Validate roles - only author and reviewer can self-register
    const allowedSelfRegisterRoles = ['author', 'reviewer'];
    const requestedRoles = roles || ['author'];
    
    const invalidRoles = requestedRoles.filter(role => !allowedSelfRegisterRoles.includes(role));
    if (invalidRoles.length > 0) {
      return res.status(400).json({
        success: false,
        message: `Cannot self-register for roles: ${invalidRoles.join(', ')}. Only 'author' and 'reviewer' roles are allowed for self-registration.`
      });
    }

    // Create user
    const user = await User.create({
      firstName,
      lastName,
      email: email.toLowerCase(),
      password,
      affiliation,
      roles: requestedRoles,
      expertise,
      orcid,
      bio
    });

    // Generate token
    const token = user.generateAuthToken();

    // Send welcome email
    await sendEmail({
      to: user.email,
      subject: 'Welcome to Pundra University Journal Management System',
      template: 'welcome',
      data: {
        name: user.fullName,
        email: user.email,
        roles: user.roles.join(', ')
      }
    });

    res.status(201).json({
      success: true,
      message: 'Registration successful',
      data: {
        user: {
          id: user._id,
          firstName: user.firstName,
          lastName: user.lastName,
          fullName: user.fullName,
          email: user.email,
          affiliation: user.affiliation,
          roles: user.roles,
          orcid: user.orcid
        },
        token
      }
    });
  } catch (error) {
    next(error);
  }
};

// @desc    Login user
// @route   POST /api/auth/login
// @access  Public
exports.login = async (req, res, next) => {
  try {
    const { email, password } = req.body;

    // Validate email & password
    if (!email || !password) {
      return res.status(400).json({
        success: false,
        message: 'Please provide email and password'
      });
    }

    // Check for user (include password for comparison)
    const user = await User.findOne({ email: email.toLowerCase() }).select('+password');

    if (!user) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials'
      });
    }

    // Check if user is active
    if (!user.isActive) {
      return res.status(401).json({
        success: false,
        message: 'Your account has been deactivated. Please contact the administrator.'
      });
    }

    // Check if password matches
    const isMatch = await user.comparePassword(password);

    if (!isMatch) {
      return res.status(401).json({
        success: false,
        message: 'Invalid credentials'
      });
    }

    // Update last login
    user.lastLogin = new Date();
    await user.save({ validateBeforeSave: false });

    // Generate token
    const token = user.generateAuthToken();

    res.status(200).json({
      success: true,
      message: 'Login successful',
      data: {
        user: {
          id: user._id,
          firstName: user.firstName,
          lastName: user.lastName,
          fullName: user.fullName,
          email: user.email,
          affiliation: user.affiliation,
          roles: user.roles,
          orcid: user.orcid,
          lastLogin: user.lastLogin
        },
        token
      }
    });
  } catch (error) {
    next(error);
  }
};

